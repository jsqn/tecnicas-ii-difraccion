def blacklist(data, elements):
    filtered_data = []
    for compound in data:
        if not any(element in compound['formula'] for element in elements):
            filtered_data.append(compound)
    return filtered_data


def whitelist(data, elements):
  filtered_data = []
  for compound in data:
    if any(element in compound['formula'] for element in elements):
      filtered_data.append(compound)
  return filtered_data


